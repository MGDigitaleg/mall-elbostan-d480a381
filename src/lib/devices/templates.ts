/** Stable hash for choosing template variants per slug — same input always picks the same template. */
export function hashSlug(slug: string): number {
  let h = 0;
  for (let i = 0; i < slug.length; i++) h = (h * 31 + slug.charCodeAt(i)) >>> 0;
  return h;
}

/** Pick from a list using a slug hash + offset (for variant diversity). */
export function pick<T>(arr: T[], slug: string, offset = 0): T {
  return arr[(hashSlug(slug) + offset) % arr.length];
}

