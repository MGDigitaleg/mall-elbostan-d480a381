export function pickFaqs(slug: string, count = 4): DeviceFAQ[] {
  const h = hashSlug(slug);
  const out: DeviceFAQ[] = [];
  const used = new Set<number>();
  for (let i = 0; out.length < count && i < FAQ_BANK.length * 2; i++) {
    const idx = (h + i * 7) % FAQ_BANK.length;
    if (used.has(idx)) continue;
    used.add(idx);
    out.push(FAQ_BANK[idx]);
  }
  return out;
}
